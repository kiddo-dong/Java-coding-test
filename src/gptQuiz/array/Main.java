package gptQuiz.array;

public class Main {
    public static void main(String[] args) {

        // 전체 순회
        int[] arr = {3,5,2,8,1};
        for(int i = 0; i < arr.length; i++){
            System.out.println(arr[i]);
        }

        for(int instance : arr){
            System.out.println(instance);
        }


    }
}
